    RCON = [
        0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36
    ]

    def __init__(self, master_key):
        """master_key должен быть списком из 16 байт или объектом bytes."""
        self.round_keys = self.key_expansion(master_key)

    def key_expansion(self, key):
        """Развертывание ключа для AES-128."""
        key_symbols = list(key)
        # Ключ делится на 4 слова (по 4 байта каждое)
        w = [key_symbols[i:i+4] for i in range(0, 16, 4)]
        
        for i in range(4, 44): # 4*11 = 44 слова для 10 раундов + 1 входной
            temp = w[i-1][:]
            if i % 4 == 0:
                # RotWord
                temp = temp[1:] + temp[:1]
                # SubWord
                temp = [self.SBOX[b] for b in temp]
                # XOR с Rcon
                temp[0] ^= self.RCON[i // 4]
            
            # w[i] = w[i-4] XOR temp
            w.append([w[i-4][j] ^ temp[j] for j in range(4)])
        
        return w

    def _sub_bytes(self, state):
        for r in range(4):
            for c in range(4):
                state[r][c] = self.SBOX[state[r][c]]

    def _inv_sub_bytes(self, state):
        for r in range(4):
            for c in range(4):
                state[r][c] = self.INV_SBOX[state[r][c]]

    def _shift_rows(self, state):
        # r=0: нет сдвига
        # r=1: сдвиг на 1 влево
        state[1] = state[1][1:] + state[1][:1]
        # r=2: сдвиг на 2 влево
        state[2] = state[2][2:] + state[2][:2]
        # r=3: сдвиг на 3 влево
        state[3] = state[3][3:] + state[3][:3]

    def _inv_shift_rows(self, state):
        state[1] = state[1][-1:] + state[1][:-1]
        state[2] = state[2][-2:] + state[2][:-2]
        state[3] = state[3][-3:] + state[3][:-3]

    @staticmethod
    def galois_mul(a, b):
        """Умножение в поле GF(2^8)."""
        p = 0
        for i in range(8):
            if b & 1:
                p ^= a
            hi_bit_set = a & 0x80
            a <<= 1
            if hi_bit_set:
                a ^= 0x1b # Неприводимый многочлен x^8 + x^4 + x^3 + x + 1
            b >>= 1
        return p & 0xFF

    def _mix_columns(self, state):
        for c in range(4):
            s0 = state[0][c]
            s1 = state[1][c]
            s2 = state[2][c]
            s3 = state[3][c]
            
            state[0][c] = self.galois_mul(0x02, s0) ^ self.galois_mul(0x03, s1) ^ s2 ^ s3
            state[1][c] = s0 ^ self.galois_mul(0x02, s1) ^ self.galois_mul(0x03, s2) ^ s3
            state[2][c] = s0 ^ s1 ^ self.galois_mul(0x02, s2) ^ self.galois_mul(0x03, s3)
            state[3][c] = self.galois_mul(0x03, s0) ^ s1 ^ s2 ^ self.galois_mul(0x02, s3)

    def _inv_mix_columns(self, state):
        for c in range(4):
            s0 = state[0][c]
            s1 = state[1][c]
            s2 = state[2][c]
            s3 = state[3][c]
            
            state[0][c] = self.galois_mul(0x0e, s0) ^ self.galois_mul(0x0b, s1) ^ self.galois_mul(0x0d, s2) ^ self.galois_mul(0x09, s3)
            state[1][c] = self.galois_mul(0x09, s0) ^ self.galois_mul(0x0e, s1) ^ self.galois_mul(0x0b, s2) ^ self.galois_mul(0x0d, s3)
            state[2][c] = self.galois_mul(0x0d, s0) ^ self.galois_mul(0x09, s1) ^ self.galois_mul(0x0e, s2) ^ self.galois_mul(0x0b, s3)
            state[3][c] = self.galois_mul(0x0b, s0) ^ self.galois_mul(0x0d, s1) ^ self.galois_mul(0x09, s2) ^ self.galois_mul(0x0e, s3)

    def _add_round_key(self, state, round_num):
        for c in range(4):
            key_word = self.round_keys[round_num * 4 + c]
            for r in range(4):
                state[r][c] ^= key_word[r]

    def encrypt(self, plain_text):
        """Шифрование 16 байт данных."""
        if len(plain_text) != 16:
            raise ValueError("Размер блока должен быть 16 байт")
            
        # Формирование State матрицы (4x4, заполнение по столбцам)
        state = [[0]*4 for _ in range(4)]
        for i in range(16):
            state[i % 4][i // 4] = plain_text[i]
            
        # Начальный раунд
        self._add_round_key(state, 0)
        
        # Основные раунды (1-9)
        for r in range(1, 10):
            self._sub_bytes(state)
            self._shift_rows(state)
            self._mix_columns(state)
            self._add_round_key(state, r)
            
        # Финальный раунд (без MixColumns)
        self._sub_bytes(state)
        self._shift_rows(state)
        self._add_round_key(state, 10)
        
        # Преобразование State матрицы обратно в байты
        output = [0]*16
        for i in range(16):
            output[i] = state[i % 4][i // 4]
        return bytes(output)

    def decrypt(self, cipher_text):
        """Расшифрование 16 байт данных."""
        if len(cipher_text) != 16:
            raise ValueError("Размер блока должен быть 16 байт")

        state = [[0]*4 for _ in range(4)]
        for i in range(16):
            state[i % 4][i // 4] = cipher_text[i]
            
        # Начальный раунд
        self._add_round_key(state, 10)
        self._inv_shift_rows(state)
        self._inv_sub_bytes(state)
        
        # Обратные раунды (9-1)
        for r in range(9, 0, -1):
            self._add_round_key(state, r)
            self._inv_mix_columns(state)
            self._inv_shift_rows(state)
            self._inv_sub_bytes(state)
            
        # Финальный раунд
        self._add_round_key(state, 0)
        
        output = [0]*16
        for i in range(16):
            output[i] = state[i % 4][i // 4]
        return bytes(output)

